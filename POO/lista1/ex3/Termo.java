public class Termo {
    private int expoente;
    private double coeficiente;

    public Termo(int expoente, double coeficiente) {
        this.expoente = expoente;
        this.coeficiente = coeficiente;
    }

    public int getExpoente() {
        return expoente;
    }

    public double getCoeficiente() {
        return coeficiente;
    }
}
